# Função para somar dois números
# definindo uma função (def)
def soma(num1, num2): 
    soma1 = num1
    soma2 = num2
    soma_total = soma1 + soma2
    print(soma_total)

# chamando a função
soma(5, 3)
soma(6.5, 4.5)
soma(10, 20)


