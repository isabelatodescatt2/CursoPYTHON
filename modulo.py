# Função para somar dois números
# definindo uma função (def)
def soma(num1, num2): 
    soma1 = num1
    soma2 = num2
    soma_total = soma1 + soma2
    print(soma_total)

# Função para subtrair dois números
def subtracao(num1, num2):
    sub1 = num1
    sub2 = num2
    sub_total = sub1 - sub2
    print(sub_total)

def multiplicacao(num1, num2):
    mult1 = num1
    mult2 = num2
    mult_total = mult1 * mult2
    print(mult_total)

def divisao(num1, num2):
    div = num1
    div2 = num2
    div_total = div / div2
    print(div_total)

# chamando as funções
soma(5, 3)
subtracao(6.5, 4.5)
multiplicacao(10, 20)
divisao(5, 3)